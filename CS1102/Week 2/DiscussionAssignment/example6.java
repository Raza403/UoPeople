public class example6 {
  public static void main(String[] args){
    // Initial message.
    System.out.println("Firing the missile in T -5");
    // Declaring i = 5
    // checking if i is greater than or equal to zero?
    // decrementing the i to avoid infinite loop.
    for (int i=5; i>=0; i--){
      System.out.println(i);
    }
    System.out.println("FIRE.....");
  }
}
