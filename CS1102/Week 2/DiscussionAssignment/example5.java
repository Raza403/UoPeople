public class example5 {
  public static void main (String[] args){
    // Initialize int i to the value of 5.
    int i = 5;
    // Initial message.
    System.out.println("Firing the missile in T -5");
    // if i is greater than or equal to 0, run the statement
    // inside the curly braces.
    while(i>=0){
      System.out.println(i);
      // decrement the i
      i--;
    }
    System.out.println("FIRE.....");
  }
}
